/**
 * Build-time generator for the PICTURE_BOX seed: draw one AI image per subject
 * (Bedrock Stability), shrink to WebP (sharp), upload to the permanent files.jpc.io
 * image host, and write the reconciled fixture (subject + accepted spellings +
 * permanent image URL) to amplify/quizgen/fixtures/pictureBoxImages.ts.
 *
 * Landmarks (not real people): Stability draws them beautifully AND they're
 * instantly recognizable, so the played quiz shows real, guessable AI art rather
 * than emoji stand-ins. Run once with editor AWS creds:
 *   AWS_PROFILE=personal npx tsx scripts/genPictureBox.ts
 * Idempotent-ish: re-running redraws + re-uploads under fresh hashes.
 */
import { randomBytes } from 'node:crypto';
import { writeFileSync } from 'node:fs';
import { S3Client, PutObjectCommand } from '@aws-sdk/client-s3';
import { generateImage } from '../amplify/deckgen/shared/bedrock';
import { resizeWebp, CARD_IMAGE_SIZE } from '../amplify/deckgen/shared/resizeImage';

