async function uploadImage(webp: Uint8Array, display: string): Promise<string> {
  const slug = display.toLowerCase().replace(/[^a-z0-9]+/g, '-');
  const hash = randomBytes(3).toString('hex');
  const name = `${hash}-${slug}.webp`;
  await s3.send(
    new PutObjectCommand({
      Bucket: HOST_BUCKET,
      Key: `public/public/${name}`,
      Body: Buffer.from(webp),
      ContentType: 'image/webp',
    }),
  );
  return `https://files.jpc.io/d/${name}`;
}

async function main(): Promise<void> {
  const rows: Array<Subject & { image: string }> = [];
  for (const s of SUBJECTS) {
    process.stdout.write(`Drawing ${s.display}… `);
    const raw = await generateImage(
      `${s.prompt}. Photorealistic, vibrant, centered, no text, no words, no watermark.`,
    );
    const webp = await resizeWebp(raw, CARD_IMAGE_SIZE);
    const image = await uploadImage(webp, s.display);
    rows.push({ ...s, image });
    console.log(image);
  }

  const file = `/**
 * GENERATED by scripts/genPictureBox.ts — do not edit by hand.
 * AI-drawn landmark images (Bedrock Stability) hosted permanently on files.jpc.io.
 * Regenerate with \`AWS_PROFILE=personal npx tsx scripts/genPictureBox.ts\`. DATA.
 */
export interface PictureBoxSubject {
  display: string;
  accepted: string[];
  hint: string;
  image: string;
}

export const PICTURE_BOX_SUBJECTS: PictureBoxSubject[] = ${JSON.stringify(
    rows.map(({ display, accepted, hint, image }) => ({ display, accepted, hint, image })),
    null,
    2,
  )};
`;
  const out = 'amplify/quizgen/fixtures/pictureBoxImages.ts';
  writeFileSync(out, file);
  console.log(`\nWrote ${rows.length} subjects → ${out}`);
}

void main();
